package com.xzy.rxjava2retrofitdemo.entity;

/**
 * 接口返回的数据对象
 */
public class Subject {
    private String name;
    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
